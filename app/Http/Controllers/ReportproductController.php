     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data = DB::table('transaction_items')
        ->join('products','transaction_items.product_id','=','products.idproducts')
        ->select('products.idproducts','products.nama', DB::raw('sum(transaction_items.jumlah) as jumlah'))
        ->groupBy('products.idproducts','products.nama')
        ->get();

        return view('reportjumlah.index', compact('data'));
    }

